#A data type tells us what kind of data is being stored in a variable.
#int → whole numbers (e.g., 10, -5)
int=20
print(type(int))

#float → decimal numbers (e.g., 3.14, -2.5)
f=20.4
print(type(f))

#str → text/characters (e.g., "Hello", 'Python')
str="ABC"
print(type(str))

#bool → True/False values
bool=True
print(type(bool))